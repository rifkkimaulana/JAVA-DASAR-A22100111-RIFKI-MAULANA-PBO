public class Augmented_Assignments {
    public static void main (String args[]) {
        int c = 100;
        
        c += 10;
        System.out.println(c);
        
        c -= 10;
        System.out.println(c);
        
        c *= 10;
        System.out.println(c);
        
        c /= 10;
        System.out.println(c);
        
        c %= 10;
        System.out.println(c);
        
    }
    
}
